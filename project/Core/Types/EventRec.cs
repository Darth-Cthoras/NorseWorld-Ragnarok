namespace NWR.Core.Types
{
	public sealed class EventRec
	{
		public string Name;
		public EventFlags Flags;
		public int Priority;

		public static EventRec Create(string name, EventFlags flags, int priority)
		{
		    EventRec result = new EventRec();
			result.Name = name;
			result.Flags = flags;
			result.Priority = priority;
			return result;
		}
	}
}
